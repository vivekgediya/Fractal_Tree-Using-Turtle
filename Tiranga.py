import turtle

V = turtle.Turtle()
V.speed(10)

for i in range(24):
    V.pencolor('blue')
    V.pensize(2)
    V.forward(118)
    V.penup()
    V.setposition(0,0)
    V.pendown()
    V.right(15)
V.setposition(0,-118)
V.circle(118)
V.penup()
V.pencolor('orange')
V.setposition(-800,120)
V.pendown()
V.fillcolor('orange')
V.begin_fill()
V.forward(1500)
V.left(90)
V.forward(300)
V.left(90)
V.forward(1500)
V.left(90)
V.forward(300)
V.end_fill()
V.penup()
V.pencolor('green')
V.setposition(-800,-120)
V.pendown()
V.fillcolor('green')
V.begin_fill()
V.right(270)
V.forward(1500)
V.right(90)
V.forward(300)
V.right(90)
V.forward(1500)
V.right(90)
V.forward(300)
V.end_fill()

turtle.done()
